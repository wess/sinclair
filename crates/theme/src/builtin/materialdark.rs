//! Material Dark (oceanic) - Material Theme terminal palette.

use crate::rgb::Rgb;
use crate::scheme::Scheme;

const fn c(r: u8, g: u8, b: u8) -> Rgb {
    Rgb::new(r, g, b)
}

pub static MATERIAL_DARK: Scheme = Scheme {
    name: "Material Dark",
    ansi: [
        c(0x00, 0x00, 0x00), // 0 black
        c(0xff, 0x53, 0x70), // 1 red
        c(0xc3, 0xe8, 0x8d), // 2 green
        c(0xff, 0xcb, 0x6b), // 3 yellow
        c(0x82, 0xaa, 0xff), // 4 blue
        c(0xc7, 0x92, 0xea), // 5 magenta (purple)
        c(0x89, 0xdd, 0xff), // 6 cyan
        c(0xff, 0xff, 0xff), // 7 white
        c(0x54, 0x6e, 0x7a), // 8 bright black (comment)
        c(0xff, 0x53, 0x70), // 9 bright red
        c(0xc3, 0xe8, 0x8d), // 10 bright green
        c(0xff, 0xcb, 0x6b), // 11 bright yellow
        c(0x82, 0xaa, 0xff), // 12 bright blue
        c(0xc7, 0x92, 0xea), // 13 bright magenta
        c(0x89, 0xdd, 0xff), // 14 bright cyan
        c(0xff, 0xff, 0xff), // 15 bright white
    ],
    foreground: c(0xee, 0xff, 0xff),
    background: c(0x26, 0x32, 0x38),
    cursor: c(0xff, 0xcc, 0x00),
    cursor_text: c(0x26, 0x32, 0x38),
    selection_foreground: c(0xff, 0xff, 0xff),
    selection_background: c(0x54, 0x6e, 0x7a),
};
