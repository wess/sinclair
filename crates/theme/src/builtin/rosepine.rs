//! Rose Pine (main) - official rose-pine terminal palette.

use crate::rgb::Rgb;
use crate::scheme::Scheme;

const fn c(r: u8, g: u8, b: u8) -> Rgb {
    Rgb::new(r, g, b)
}

pub static ROSE_PINE: Scheme = Scheme {
    name: "Rose Pine",
    ansi: [
        c(0x26, 0x23, 0x3a), // 0 black (overlay)
        c(0xeb, 0x6f, 0x92), // 1 red (love)
        c(0x31, 0x74, 0x8f), // 2 green (pine)
        c(0xf6, 0xc1, 0x77), // 3 yellow (gold)
        c(0x9c, 0xcf, 0xd8), // 4 blue (foam)
        c(0xc4, 0xa7, 0xe7), // 5 magenta (iris)
        c(0xeb, 0xbc, 0xba), // 6 cyan (rose)
        c(0xe0, 0xde, 0xf4), // 7 white (text)
        c(0x6e, 0x6a, 0x86), // 8 bright black (muted)
        c(0xeb, 0x6f, 0x92), // 9 bright red
        c(0x31, 0x74, 0x8f), // 10 bright green
        c(0xf6, 0xc1, 0x77), // 11 bright yellow
        c(0x9c, 0xcf, 0xd8), // 12 bright blue
        c(0xc4, 0xa7, 0xe7), // 13 bright magenta
        c(0xeb, 0xbc, 0xba), // 14 bright cyan
        c(0xe0, 0xde, 0xf4), // 15 bright white
    ],
    foreground: c(0xe0, 0xde, 0xf4),
    background: c(0x19, 0x17, 0x24),
    cursor: c(0x52, 0x4f, 0x67), // highlight high
    cursor_text: c(0xe0, 0xde, 0xf4),
    selection_foreground: c(0xe0, 0xde, 0xf4),
    selection_background: c(0x40, 0x3d, 0x52), // highlight med
};
