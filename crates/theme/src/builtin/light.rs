//! Default light scheme: white background with ANSI colors darkened
//! for readability on light surfaces.

use crate::rgb::Rgb;
use crate::scheme::Scheme;

const fn c(r: u8, g: u8, b: u8) -> Rgb {
    Rgb::new(r, g, b)
}

pub static LIGHT: Scheme = Scheme {
    name: "light",
    ansi: [
        c(0x00, 0x00, 0x00), // 0 black
        c(0x99, 0x00, 0x00), // 1 red
        c(0x00, 0xa6, 0x00), // 2 green
        c(0x99, 0x99, 0x00), // 3 yellow
        c(0x00, 0x00, 0xb2), // 4 blue
        c(0xb2, 0x00, 0xb2), // 5 magenta
        c(0x00, 0xa6, 0xb2), // 6 cyan
        c(0xbf, 0xbf, 0xbf), // 7 white
        c(0x66, 0x66, 0x66), // 8 bright black
        c(0xe5, 0x00, 0x00), // 9 bright red
        c(0x00, 0xd9, 0x00), // 10 bright green
        c(0xe5, 0xe5, 0x00), // 11 bright yellow
        c(0x00, 0x00, 0xff), // 12 bright blue
        c(0xe5, 0x00, 0xe5), // 13 bright magenta
        c(0x00, 0xe5, 0xe5), // 14 bright cyan
        c(0xe5, 0xe5, 0xe5), // 15 bright white
    ],
    foreground: c(0x00, 0x00, 0x00),
    background: c(0xff, 0xff, 0xff),
    cursor: c(0x00, 0x00, 0x00),
    cursor_text: c(0xff, 0xff, 0xff),
    selection_foreground: c(0x00, 0x00, 0x00),
    selection_background: c(0xb4, 0xd5, 0xff),
};

