//! Default dark scheme: Ghostty-like dark background with the
//! standard xterm ANSI 16.

use crate::rgb::Rgb;
use crate::scheme::Scheme;

const fn c(r: u8, g: u8, b: u8) -> Rgb {
    Rgb::new(r, g, b)
}

pub static DARK: Scheme = Scheme {
    name: "dark",
    ansi: [
        c(0x00, 0x00, 0x00), // 0 black
        c(0xcd, 0x00, 0x00), // 1 red
        c(0x00, 0xcd, 0x00), // 2 green
        c(0xcd, 0xcd, 0x00), // 3 yellow
        c(0x00, 0x00, 0xee), // 4 blue
        c(0xcd, 0x00, 0xcd), // 5 magenta
        c(0x00, 0xcd, 0xcd), // 6 cyan
        c(0xe5, 0xe5, 0xe5), // 7 white
        c(0x7f, 0x7f, 0x7f), // 8 bright black
        c(0xff, 0x00, 0x00), // 9 bright red
        c(0x00, 0xff, 0x00), // 10 bright green
        c(0xff, 0xff, 0x00), // 11 bright yellow
        c(0x5c, 0x5c, 0xff), // 12 bright blue
        c(0xff, 0x00, 0xff), // 13 bright magenta
        c(0x00, 0xff, 0xff), // 14 bright cyan
        c(0xff, 0xff, 0xff), // 15 bright white
    ],
    foreground: c(0xff, 0xff, 0xff),
    background: c(0x28, 0x2c, 0x34),
    cursor: c(0xff, 0xff, 0xff),
    cursor_text: c(0x28, 0x2c, 0x34),
    selection_foreground: c(0xff, 0xff, 0xff),
    selection_background: c(0x3e, 0x44, 0x51),
};

