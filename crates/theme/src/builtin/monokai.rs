//! Monokai - Wimer Hazenberg's palette (base16 terminal mapping).

use crate::rgb::Rgb;
use crate::scheme::Scheme;

const fn c(r: u8, g: u8, b: u8) -> Rgb {
    Rgb::new(r, g, b)
}

pub static MONOKAI: Scheme = Scheme {
    name: "Monokai",
    ansi: [
        c(0x27, 0x28, 0x22), // 0 black
        c(0xf9, 0x26, 0x72), // 1 red
        c(0xa6, 0xe2, 0x2e), // 2 green
        c(0xf4, 0xbf, 0x75), // 3 yellow
        c(0x66, 0xd9, 0xef), // 4 blue
        c(0xae, 0x81, 0xff), // 5 magenta
        c(0xa1, 0xef, 0xe4), // 6 cyan
        c(0xf8, 0xf8, 0xf2), // 7 white
        c(0x75, 0x71, 0x5e), // 8 bright black (comment)
        c(0xf9, 0x26, 0x72), // 9 bright red
        c(0xa6, 0xe2, 0x2e), // 10 bright green
        c(0xf4, 0xbf, 0x75), // 11 bright yellow
        c(0x66, 0xd9, 0xef), // 12 bright blue
        c(0xae, 0x81, 0xff), // 13 bright magenta
        c(0xa1, 0xef, 0xe4), // 14 bright cyan
        c(0xf9, 0xf8, 0xf5), // 15 bright white
    ],
    foreground: c(0xf8, 0xf8, 0xf2),
    background: c(0x27, 0x28, 0x22),
    cursor: c(0xf8, 0xf8, 0xf2),
    cursor_text: c(0x27, 0x28, 0x22),
    selection_foreground: c(0xf8, 0xf8, 0xf2),
    selection_background: c(0x49, 0x48, 0x3e),
};
