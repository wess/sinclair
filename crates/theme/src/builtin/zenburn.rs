//! Zenburn - Jani Nurminen's low-contrast palette (terminal port).

use crate::rgb::Rgb;
use crate::scheme::Scheme;

const fn c(r: u8, g: u8, b: u8) -> Rgb {
    Rgb::new(r, g, b)
}

pub static ZENBURN: Scheme = Scheme {
    name: "Zenburn",
    ansi: [
        c(0x4d, 0x4d, 0x4d), // 0 black
        c(0x70, 0x50, 0x50), // 1 red
        c(0x60, 0xb4, 0x8a), // 2 green
        c(0xf0, 0xdf, 0xaf), // 3 yellow
        c(0x50, 0x60, 0x70), // 4 blue
        c(0xdc, 0x8c, 0xc3), // 5 magenta
        c(0x8c, 0xd0, 0xd3), // 6 cyan
        c(0xdc, 0xdc, 0xcc), // 7 white
        c(0x70, 0x90, 0x80), // 8 bright black
        c(0xdc, 0xa3, 0xa3), // 9 bright red
        c(0xc3, 0xbf, 0x9f), // 10 bright green
        c(0xe0, 0xcf, 0x9f), // 11 bright yellow
        c(0x94, 0xbf, 0xf3), // 12 bright blue
        c(0xec, 0x93, 0xd3), // 13 bright magenta
        c(0x93, 0xe0, 0xe3), // 14 bright cyan
        c(0xff, 0xff, 0xff), // 15 bright white
    ],
    foreground: c(0xdc, 0xdc, 0xcc),
    background: c(0x3f, 0x3f, 0x3f),
    cursor: c(0xdc, 0xdc, 0xcc),
    cursor_text: c(0x3f, 0x3f, 0x3f),
    selection_foreground: c(0xdc, 0xdc, 0xcc),
    selection_background: c(0x2f, 0x2f, 0x2f),
};
