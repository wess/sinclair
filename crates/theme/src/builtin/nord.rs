//! Nord - official terminal mapping (nordtheme.com, nord0..nord15).

use crate::rgb::Rgb;
use crate::scheme::Scheme;

const fn c(r: u8, g: u8, b: u8) -> Rgb {
    Rgb::new(r, g, b)
}

pub static NORD: Scheme = Scheme {
    name: "Nord",
    ansi: [
        c(0x3b, 0x42, 0x52), // 0 black (nord1)
        c(0xbf, 0x61, 0x6a), // 1 red (nord11)
        c(0xa3, 0xbe, 0x8c), // 2 green (nord14)
        c(0xeb, 0xcb, 0x8b), // 3 yellow (nord13)
        c(0x81, 0xa1, 0xc1), // 4 blue (nord9)
        c(0xb4, 0x8e, 0xad), // 5 magenta (nord15)
        c(0x88, 0xc0, 0xd0), // 6 cyan (nord8)
        c(0xe5, 0xe9, 0xf0), // 7 white (nord5)
        c(0x4c, 0x56, 0x6a), // 8 bright black (nord3)
        c(0xbf, 0x61, 0x6a), // 9 bright red
        c(0xa3, 0xbe, 0x8c), // 10 bright green
        c(0xeb, 0xcb, 0x8b), // 11 bright yellow
        c(0x81, 0xa1, 0xc1), // 12 bright blue
        c(0xb4, 0x8e, 0xad), // 13 bright magenta
        c(0x8f, 0xbc, 0xbb), // 14 bright cyan (nord7)
        c(0xec, 0xef, 0xf4), // 15 bright white (nord6)
    ],
    foreground: c(0xd8, 0xde, 0xe9),
    background: c(0x2e, 0x34, 0x40),
    cursor: c(0xd8, 0xde, 0xe9),
    cursor_text: c(0x2e, 0x34, 0x40),
    selection_foreground: c(0xd8, 0xde, 0xe9),
    selection_background: c(0x4c, 0x56, 0x6a),
};
